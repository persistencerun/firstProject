package com.first.two2;

public class Which {
	
	
	public static void main(String[] args){
		//while
		int i=0;
		//第一种
		while(i<5){
			System.out.println("数字"+i);
			i=i+1;
		}
		
		//第二种
		do{
			System.out.println("第二种数字"+i);
			i=i+1;
		}while(i<10);
		
		
		
		//第三种
		for(int i1=0;i1<10;i1++){
			//i1++   等于 i1=i1+1
			System.out.println("第三种数字"+i1);
		}
		
		//第四种   集合
		//foreach()
		
		
		//第五种    递归
		
		
	}

}
